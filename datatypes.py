# Fundamental Datatypes

# int
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print("")

# float
print(type(2 / 4))
print(type(5.43))
print(type(9.9 + 1.1))
print("")

# bool
print(type(True))
print(type(False))
print("")

# str
print(type("Hello World"))
print("")

# list
print(type([1, 'list', 0.45]))

# tuple
print(type(('name', 123, 0.545,)))

# set
print(type({"apple", "banana", "cherry"}))

# dict
print(type({"hello": "world"}))

